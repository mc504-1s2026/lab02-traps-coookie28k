# this is what will be loaded into the stvec CSR; the
# trap handler address must be 4-byte aligned:
# https://docs.riscv.org/reference/isa/priv/supervisor.html#12-1-1-2-supervisor-trap-vector-base-address-stvec-register
.globl trap_entry
.align 4
trap_entry:

    # your code here; save registers to the stack
    addi sp, sp, -144
    sd ra, 0(sp)
    sd gp, 8(sp)
    sd tp, 16(sp)
    sd t0, 24(sp)
    sd t1, 32(sp)
    sd t2, 40(sp)
    sd a0, 48(sp)
    sd a1, 56(sp)
    sd a2, 64(sp)
    sd a3, 72(sp)
    sd a4, 80(sp) 
    sd a5, 88(sp)
    sd a6, 96(sp)
    sd a7, 104(sp)
    sd t3, 112(sp)
    sd t4, 120(sp)
    sd t5, 128(sp)
    sd t6, 136(sp)

    call handle_trap

    # your code here; restore registers
    ld ra,   0(sp)
    ld gp,   8(sp)
    ld tp,  16(sp)
    ld t0,  24(sp)
    ld t1,  32(sp)
    ld t2,  40(sp)
    ld a0,  48(sp)
    ld a1,  56(sp)
    ld a2,  64(sp)
    ld a3,  72(sp)
    ld a4,  80(sp)
    ld a5,  88(sp)
    ld a6,  96(sp)
    ld a7, 104(sp)
    ld t3, 112(sp)
    ld t4, 120(sp)
    ld t5, 128(sp)
    ld t6, 136(sp)

    addi sp, sp, 144

    sret
